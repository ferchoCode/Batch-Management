     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            Cliente::insertCliente($request);

            return  redirect()->to('cliente')->with('success','cliente registrado correctamente');
        } catch (\Throwable $th) {
            return redirect()->to('cliente/create')->with('error', $th->errorInfo[2]);
        }
    }

